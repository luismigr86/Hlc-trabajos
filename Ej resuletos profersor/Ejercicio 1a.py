""" Este superprograma se encarga del procesado masivo
de dos números para, con una potencia de cálculo enorme, sumarlos
y restarlos offline ;-P"""


num1 = 0    # Variable numérica para introducir el primer número.
num2 = 0    # Variable numérica para introducir el segundo número.
num1 = int(input("Introduce el primer número: "))   # Pido por teclado el primer número.
num2 = int(input("Introduce el segundo número: "))  # Pido por teclado el segundo número.  
print("La suma de ambos números es: ", num1 + num2) # Muestro por pantalla la suma.
print("La resta de ambos números es: ", num1 - num2)    # Muestro por pantalla la resta.
